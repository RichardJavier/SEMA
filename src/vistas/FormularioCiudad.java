    public FormularioCiudad(FrmCiudad parent, boolean modal, Integer idCiudad) {
        FormularioCiudad.frmCiudad = parent;
        this.setModal(modal);
        initComponents();
        this.idCiudad = idCiudad;
        
        try {
            if (idCiudad > 0) {
                CiudadDao ciudadDao = new CiudadDao();
                resultSet = ciudadDao.consultaCiudad(idCiudad);
                while (resultSet.next()) {
                    ciudadTxt.setText(resultSet.getString("ciudad"));
                }
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }

    }


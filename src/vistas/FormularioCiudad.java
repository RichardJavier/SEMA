    private void guardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtnActionPerformed
        if (validaForm()) {
            Crud crud = new Crud();
            Map campos = new HashMap();
            campos.put("ciudad", ciudadTxt.getText().toUpperCase());
            campos.put("id_canton", frmCiudad.getCod()+1);
            if (idCiudad == 0) {
                crud.insertarM("ciudad", campos, Ingreso.getUsuario().getNombre());
                this.dispose();
            } else {
                crud.actualizarM("ciudad", "id_ciudad", idCiudad, campos, Ingreso.getUsuario().getNombre());
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error campos vacios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_guardarBtnActionPerformed


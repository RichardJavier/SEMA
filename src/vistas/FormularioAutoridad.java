    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        campos = new HashMap();
        Calendar cal = Calendar.getInstance();
        if (validaform()==true) {
            campos.put("nombre",nombresTxt.getText().toUpperCase());
            campos.put("cargo",cargoTxt.getText().toUpperCase());
            campos.put("fecha_creacion", cal.getTime());
            crud=new Crud();
            if (idAutoridad==0) {
                System.out.println(Ingreso.getUsuario().getNombre());
                crud.insertarM("autoridades", campos,Ingreso.getUsuario().getNombre());
                this.dispose();
            } else {
                crud.actualizarM("autoridades","idautoridades",idAutoridad, campos,Ingreso.getUsuario().getNombre());
                this.dispose();
            }
        }else {
            JOptionPane.showMessageDialog(null,"Error campos vacios","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


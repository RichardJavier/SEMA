    public FormularioAutoridad(FrmAutoridad parent, boolean modal,Integer idAutoridad ) {
        FormularioAutoridad.frmAutoridad=parent;
        this.setModal(modal);
        initComponents();
        this.idAutoridad=idAutoridad;
        autoridad=new Autoridad();
        autoridadesDao=new AutoridadesDao();
       
        try {
            if(idAutoridad > 0){
                resultSet=autoridadesDao.cargaAutoridades(idAutoridad);
                while(resultSet.next()){
                    autoridad.setNombre(resultSet.getString("nombre"));
                    autoridad.setCargo(resultSet.getString("cargo"));
                    nombresTxt.setText(autoridad.getNombre());
                    cargoTxt.setText(autoridad.getCargo());
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }


    private boolean validaForm() {
        boolean respuesta = true;
        i = (double) valNotaSpinner.getValue();
        y = (double) valTutoSpinner.getValue();
        double valor = i + y;
        double numero = 100;
        if (nombreMallaTxt.getText().length() == 0) {
            respuesta = false;
        } else if (semestreCmb.getSelectedIndex() == 0) {
            respuesta = false;
        } else if (especialidadCmb.getSelectedIndex() == 0) {
            respuesta = false;
        } else if (periodoCmb.getSelectedIndex() == 0) {
            respuesta = false;
        }  else if (valor > numero) {
            respuesta = false;
        } else if (numHorasTxt.getText().length() == 0) {
            respuesta = false;
        } else if (actMallaRdb.isSelected() == false && dscMallaRdb.isSelected() == false) {
            respuesta = false;
        } else {
            respuesta = true;
        }

        return respuesta;
    }

    private void cargarSemestre() {

        try {
            String semes;
            rs = mallaControl.semestre();
            while (rs.next()) {
                semes = rs.getString("semestre");
                semestreCmb.addItem(semes);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormularioMalla.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void cargarEspecialidad() {
        try {
            String espe;
            rs = mallaControl.especialidad();
            while (rs.next()) {
                espe = rs.getString("especialidad");
                especialidadCmb.addItem(espe);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormularioMalla.class.getName()).log(Level.SEVERE, null, ex);

        }
    }

    private void cargarPeriodo() {
        try {
            rs = mallaControl.periodo();
            dps = new DatosPeriodoSemestre();
            while (rs.next()) {
                dps.setId1Periodo(Double.valueOf(rs.getString("id1_periodo")));
                dps.setPeriodo(rs.getString("periodo"));
                periodoCmb.addItem(dps);

            }
        } catch (SQLException ex) {
            Logger.getLogger(FormularioMalla.class.getName()).log(Level.SEVERE, null, ex);

        }
    }

    
    


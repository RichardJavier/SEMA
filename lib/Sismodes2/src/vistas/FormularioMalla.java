    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Map datos = new HashMap();
        Calendar cal = Calendar.getInstance();
        datos.put("nombre_malla", nombreMallaTxt.getText().toUpperCase());
        datos.put("id1_semestre", semestreCmb.getSelectedIndex());
        datos.put("id1_especialidad", especialidadCmb.getSelectedIndex());
        datos.put("id1_periodo", dps.getId1Periodo());
        datos.put("por_nota", (double) valNotaSpinner.getValue());
        datos.put("por_tuto", (double) valTutoSpinner.getValue());
        datos.put("num_hora", numHorasTxt.getText());
        datos.put("estado", activaMalla);
        datos.put("fecha_creacion", cal.getTime());
        datos.put("porcentaje", "100");
        if (validaForm()) {
            try {
                if (pkMalla == 0) {
                    //camabiaEstado();
                    mallaControl.insertar("malla", datos);
                    this.dispose();

                } else {
                    mallaControl.actualizar("malla", "id_malla", 0.0, datos);
                    this.dispose();
                    JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
                }

            } catch (Exception e) {
                Logger.getLogger(FrmMalla.class.getName()).log(Level.SEVERE, null, e);
                JOptionPane.showMessageDialog(null, "Error", "Ocurrio un error al ingresar", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error", "Campos Vacios", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

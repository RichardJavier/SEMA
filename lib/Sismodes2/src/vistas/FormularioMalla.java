    @SuppressWarnings("static-access")
    public FormularioMalla(FrmMalla parent, boolean modal, Integer pkMalla) throws SQLException {
        this.malla = parent;
        this.setModal(modal);
        initComponents();
        this.pkMalla = pkMalla;
        mallaControl = new DatosMallaControl();

        cargarEspecialidad();
        cargarPeriodo();
        cargarSemestre();
        // dps = new DatosPeriodoSemestre();
        if (pkMalla > 0) {
            rs = mallaControl.consulta(pkMalla);
            String valRadio;
            while (rs.next()) {
                nombreMallaTxt.setText(rs.getString("nombre_malla"));
                semestreCmb.setSelectedIndex(Integer.valueOf(rs.getString("id_semestre")));
                especialidadCmb.setSelectedIndex(Integer.valueOf(rs.getString("id_especialidad")));
                periodoCmb.setSelectedIndex(Integer.valueOf(rs.getString("id_periodo")));
                valNotaSpinner.setValue(Integer.parseInt(rs.getString("por_nota")));
                valTutoSpinner.setValue(Integer.parseInt(rs.getString("por_tuto")));
                numHorasTxt.setText(rs.getString("num_hora"));
                valRadio = rs.getString("estado");

                switch (valRadio) {
                    case "AC":
                        actMallaRdb.setSelected(true);
                        break;
                    case "DE":
                        dscMallaRdb.setSelected(true);
                        break;
                }
            }
            cc.desconectar();
        }
    }

